# Создаём список, каждый элемент которого будет соответствовать количеству периодов плавного спуска акций,
# включающих элемент из изначального списка с таким же индексом.

from types import List

def get_descent_periods(values: List[int]) -> int:
    """Сложность данной функции O(n)
    Args:
        values (List[int]): целочисленный массив значений
    Returns:
        int: количество периодов плавного спуска
    """        
    lst = [1] * len(values)     # изначальный список состоит из единиц, потому что списки, включающие только одно число,
                                # тоже являются периодами плавного спуска акций.
    for i in range(1, len(lst)):
        if values[i] - values[i-1] == -1:
            lst[i] += lst[i-1] 
    return sum(lst)     # ответом является сумма из получившегося списка, потому что каждый элемент созданного списка отвечает
                        # за количество периодов плавного спуска акций, включающих уникальный элемент из изначального списка, так что все 
                        # последовательности уникальны 